export function wordEmoji(word: string): string {
  let hash = 0;
  for (let i = 0; i < word.length; i++) {
    hash = word.charCodeAt(i) + ((hash << 5) - hash);
  }
  return EMOJIS[((hash % EMOJIS.length) + EMOJIS.length) % EMOJIS.length];
}
